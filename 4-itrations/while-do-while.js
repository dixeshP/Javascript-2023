// While and Do While loop

let i = 1;
while (i <= 10) {
  //   console.log(`Your currunt value is ${i}`);
  i++;
}

// Array in while loop
let arrayData = ["Dixesh", "Rajal", "Sunny", "Vivek", "Ketan"];
let arrayIndex = 0;
while (arrayIndex < arrayData.length) {
  //   console.log(`Your value is ${arrayData[arrayIndex]}`);
  arrayIndex = arrayIndex + 1;
}

// Do while loop

let a = 11;
do {
  console.log(`Hello ${a}`);
  a = a + 1;
} while (a <= 10);
